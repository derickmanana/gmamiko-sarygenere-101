export type TranslationKey = keyof (typeof dict)["fr"];

interface I18nCtx {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: TranslationKey) => string;
}

const Ctx = createContext<I18nCtx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("fr");

  useEffect(() => {
    const stored = typeof window !== "undefined" ? (localStorage.getItem("gm_locale") as Locale | null) : null;
    if (stored && ["mg", "fr", "en"].includes(stored)) setLocaleState(stored);
  }, []);

  const setLocale = (l: Locale) => {
    setLocaleState(l);
    if (typeof window !== "undefined") localStorage.setItem("gm_locale", l);
  };

  const value = useMemo<I18nCtx>(
    () => ({
      locale,
      setLocale,
      t: (k) => dict[locale][k] ?? dict.fr[k] ?? k,
    }),
    [locale],
  );

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useI18n() {
  const c = useContext(Ctx);
  if (!c) throw new Error("useI18n must be used within I18nProvider");
  return c;
}
